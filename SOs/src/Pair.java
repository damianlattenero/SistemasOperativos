
public class Pair<A, B> {
	
	A left;
	B right;
	
	public Pair(A left, B right) {
		super();
		this.left = left;
		this.right = right;
	}
	public A getLeft() {
		return left;
	}
	public void setLeft(A left) {
		this.left = left;
	}
	public B getRight() {
		return right;
	}
	public void setRight(B right) {
		this.right = right;
	}
	
	

}
